#ifndef	STD_TYPES_H
#define STD_TYPES_H

typedef unsigned char			u8;
typedef signed char 			s8;

typedef short unsigned int		u16;
typedef short signed int 		s16;

typedef unsigned int 			u32;
typedef signed int 				s32;

typedef float 					f32;
typedef double 					f64;
typedef long double				f96;

#define NULL 					(void *)0

#endif
